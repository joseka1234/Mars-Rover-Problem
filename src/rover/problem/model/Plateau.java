package rover.problem.model;

public class Plateau {
	private int Width;
	private int Height;
	
	public Plateau(int width, int height) {
		setWidth(width);
		setHeight(height);
	}
	
	// Getters and Setters
	public int getWidth() {
		return Width;
	}
	public void setWidth(int width) {
		Width = width;
	}
	public int getHeight() {
		return Height;
	}
	public void setHeight(int height) {
		Height = height;
	}
	
	
}
